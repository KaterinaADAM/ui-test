/**
 * Provides the classes necessary to manage all related common functions on Wiki.
 *  
 * <p>
 * Common functions for Wiki which need to be managed are: Basic Action, ManageDraft, Permalink, Permission, RichTextMode, Template, Version and WikiBase.
 * <ul>
 * <li>The class Basic Action includes all basic methods of creating, editing and deleting a Wiki's page. 
 * <li>The class ManageDraft contains all methods to manage a draft of Wiki's page.
 * <li>The class Permalink contains all methods to share/restrict a page's content with other users.
 * <li>The class Permission contains all methods to manage the restrictions to apply on a wiki or a page and to specific users, groups or memberships.
 * <li>The class RichTextMode contains all methods to edit an wiki page directly in the wiki markup language.
 * <li>The class Template contains all methods to create and delete a template.
 * <li>The class Version contains all methods to manage all versions of a page when editing the content of a page.
 * <li>The class WikiBase contains all methods to manage all WebElements on Wiki.
 * </ul>
 */
package org.exoplatform.selenium.platform.wiki;